                تاريخ إرسال اسم المستخدم وكلمة المرور التجريبي. وبعد الموافقة على الشراء ودفع الفاتورة واعتماد الطلب، وبغض
                النظر عن تاريخ استلام الأجهزة أو تجربته للبرنامج من عدمها، فلا يحق له الاسترجاع للاشتراك. ويحق له إرجاع
                الأجهزة أو تسليمها لشركة الشحن المعتمدة من قبل شركة عالم التشفير خلال خمسة (5) أيام عمل من تاريخ اعتماد طلب
                الإرجاع، على أن تكون الأجهزة في حالتها السليمة مع كامل ملحقاتها، وإلا سيتم خصم قيمة الأجهزة الفعلية "قبل
                الخصم" من إجمالي المبلغ كما هو موضح في الفاتورة أو السعر الفعلي بالموقع الرسمي لشركة عالم التشفير. وينطبق
                أيضًا في حال تم الحصول على الأجهزة مجانًا أو بخصم مقابل الاشتراك في البرنامج. ويسقط حق العميل في الإرجاع عند
                مضي مدة التجربة دون تقديم طلب خلال هذه الفترة، أو انقضاء مدة خمسة (5) أيام عمل من تاريخ اعتماد الإرجاع دون
                تسليم الأجهزة لشركة الشحن المعتمدة من قبل شركة عالم التشفير أو لشركة عالم التشفير.
