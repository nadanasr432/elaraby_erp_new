                الرياض في المملكة العربية السعودية، ويجب أن تكون نتائج التحكيم غير قابلة للاستئناف وملزمة بكل الطرفين.
            </p>

            <h5 class="mb-3">15. فترة التجربة والإرجاع</h5>
